#include <stdio.h>

int main() {
    int i;
    int amount = 1;
    char sentence[amount];


    printf("Please enter your sentence: ");
    for (i = 0; i < 1; i++) {
        putchar('\n');

        scanf("%c", sentence[i]);
    }

    amount = len(sentence);

}